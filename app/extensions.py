"""
Файл: app/extensions.py
Назначение: Точка инициализации расширений Flask, которые используются в разных частях проекта.
Роль в архитектуре: инфраструктурный слой (infrastructure layer), чтобы избежать циклических импортов.

Импорты:
- flask_sqlalchemy.SQLAlchemy: ORM-обёртка над SQLAlchemy для удобной интеграции с Flask.

Связи:
- Импортируется в app/__init__.py для вызова db.init_app(app).
- Используется в app/models.py как единый объект для объявления моделей.

Почему так:
- Паттерн «расширения создаются глобально, а привязываются позже» называется application factory pattern.
- Альтернатива: создавать SQLAlchemy(app) сразу в точке создания приложения.
	Это проще для маленьких скриптов, но хуже масштабируется и усложняет тестирование.
"""

from flask_sqlalchemy import SQLAlchemy

# Единый объект ORM для всего проекта.
# **Важно**: здесь НЕ передаём app, чтобы можно было создавать приложение динамически.
db = SQLAlchemy()