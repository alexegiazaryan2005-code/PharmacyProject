#!/usr/bin/env python
# -*- coding: utf-8 -*-

"""
Файл: run.py
Назначение: Точка входа (entry point) для локального запуска Flask-приложения.
Роль в архитектуре: orchestration-скрипт — создаёт app, инициализирует БД и стартует dev-сервер.

Связи:
- Использует конфигурацию из app/__init__.py.
- Работает с моделями из app/models.py через metadata в db.
"""

"""
Импорты:
- app.create_app: фабрика приложения.
- app.extensions.db: объект ORM для вызова create_all().
- sys: завершение процесса с кодом ошибки.
"""

from app import create_app
from app.extensions import db
import sys

# def - ключевое слово для создания функции
# main - имя функции (традиционно используется как точка входа)
# (): - пустые скобки означают отсутствие параметров, двоеточие завершает объявление
def main():
    """
    Запускает приложение и выполняет первичную инициализацию базы данных.

    Параметры:
        Нет.

    Возвращает:
        None.

    Исключения:
        Не пробрасывает наружу: любые исключения перехватываются в try/except,
        после чего процесс завершается с кодом 1.

    Примеры:
        main()
        # Приложение стартует на http://127.0.0.1:5000
    """
    try:
        # это ключевое слово в Python, которое начинает блок обработки исключений (ошибок).
        # Создаем экземпляр Flask-приложения через фабричную функцию create_app()
        # Фабричная функция обычно настраивает конфигурацию, расширения и регистрирует маршруты
        app = create_app()

        # Проверяем, что приложение успешно создалось (не равно None)
        if app is None:
            print("Ошибка: не удалось создать приложение")
            sys.exit(1)  # Немедленное завершение программы с кодом ошибки 1

        # [БЛОК: инициализация БД]
        # Создаем контекст приложения Flask. Это обязательно для работы с Flask-SQLAlchemy,
        # так как многие операции требуют доступа к текущему контексту (например, к конфигурации).
        # Альтернативный подход: использовать отдельную CLI-команду типа `flask init-db`,
        # что более правильно для production-среды.

        with app.app_context():
            # Создаем все таблицы в базе данных на основе определенных моделей SQLAlchemy
            # Если таблицы уже существуют - команда их не перезапишет (по умолчанию)
            db.create_all()
            print("База данных успешно инициализирована")
            # Показываем фактический URI базы данных (полезно для отладки)
            print(f"URL базы данных: {app.config['SQLALCHEMY_DATABASE_URI']}")

        # [БЛОК: запуск сервера разработки]
        # debug=True включает режим отладки Flask:
        # - автоматическая перезагрузка при изменении кода
        # - подробные страницы с ошибками в браузере
        # - интерактивный отладчик
        # Предупреждение: НЕ ИСПОЛЬЗОВАТЬ debug=True в production из соображений безопасности!
        print("Запуск сервера разработки...")
        app.run(debug=True, host='127.0.0.1', port=5000)
        # host='127.0.0.1' - сервер слушает только локальные подключения (безопасно для разработки)
        # port=5000 - стандартный порт Flask
        # Exception — базовый класс для всех ошибок в Python (ловит любую ошибку)
    except Exception as e:
        # Ловим любые исключения, возникшие при создании или запуске приложения
        print(f"Ошибка при запуске приложения: {e}")
        sys.exit(1)  # Завершаем программу с кодом ошибки

    if __name__ == '__main__':
        # Это условие гарантирует, что main() выполнится ТОЛЬКО при прямом запуске файла
        # Если файл импортируется как модуль в другой скрипт - main() не запустится
        main()