"""
Файл: create_missing_tables.py
Назначение: Проверяет, какие таблицы отсутствуют в БД, и создаёт их.
Роль в проекте: восстановление схемы после частичных миграций или ручных изменений.
"""

# Импортируем функцию создания приложения и объект базы данных из главного модуля

from app import create_app, db

# Импортируем все модели данных (символ * импортирует все классы из модуля models)

from app.models import *

# Импортируем библиотеку для работы с SQLite (хотя в коде напрямую не используется)

import sqlite3

# Импортируем модуль для работы с операционной системой

import os





def create_missing_tables():

    """

    Создаёт недостающие таблицы и при необходимости добавляет стартовые типы рецептурности.



    Возвращает:

        None.



    Исключения:

        Явно не перехватываются на уровне функции; возможные ошибки БД будут проброшены.

    """

    # Создаем экземпляр Flask-приложения

    app = create_app()



    # Используем контекст приложения для работы с базой данных

    with app.app_context():

        # Выводим путь к базе данных из конфигурации приложения

        print(f"📁 База данных: {app.config['SQLALCHEMY_DATABASE_URI']}")



        # Создаем объект inspector для получения информации о структуре БД

        inspector = db.inspect(db.engine)

        # Получаем список всех существующих таблиц в базе данных

        existing_tables = inspector.get_table_names()

        # Выводим список существующих таблиц

        print(f"📋 Существующие таблицы: {existing_tables}")



        # [БЛОК: ожидаемые таблицы]

        # Здесь указаны имена таблиц как их «видит» БД, а не имена Python-классов.

        # Список всех таблиц, которые должны быть в базе данных

        all_models = [

            'recept',           # Таблица типов рецептурности

            'category',         # Таблица категорий товаров

            'manufacturer',     # Таблица производителей

            'product',          # Таблица товаров

            'product_category', # Связующая таблица между товарами и категориями

            'pharmacy',         # Таблица аптек

            'stock',            # Таблица остатков на складах

            'supply',           # Таблица поставок

            'sale',             # Таблица продаж

            'transfer'          # Таблица перемещений между аптеками

        ]



        # Вычисляем разницу множеств (фактически в форме list comprehension).

        # Создаем список таблиц, которые есть в all_models, но отсутствуют в existing_tables

        missing_tables = [t for t in all_models if t not in existing_tables]



        # Проверяем, есть ли отсутствующие таблицы

        if missing_tables:

            # Выводим список отсутствующих таблиц

            print(f"⚠️ Отсутствуют таблицы: {missing_tables}")

            print("🔄 Создаем недостающие таблицы...")



            # create_all() безопасен для существующих таблиц: не удаляет и не переопределяет их.

            # Создаем все таблицы, которые определены в моделях, но еще не существуют в БД

            db.create_all()



            # Проверяем результат создания таблиц

            # Снова создаем inspector для обновленной структуры БД

            inspector = db.inspect(db.engine)

            # Получаем обновленный список таблиц

            new_tables = inspector.get_table_names()

            # Выводим обновленный список таблиц

            print(f"✅ Теперь есть таблицы: {new_tables}")



            # Добавляем начальные данные для recept, если таблица была создана

            # Проверяем, что таблица recept была создана (отсутствовала ранее) и в ней нет записей

            if 'recept' not in existing_tables and Recept.query.count() == 0:

                print("📝 Добавляем начальные типы рецептурности...")

                # Создаем список объектов Recept с начальными данными

                recepts = [

                    Recept(name="Без рецепта", description="Безрецептурный препарат"),

                    Recept(name="Рецептурный", description="Отпускается по рецепту"),

                    Recept(name="Строгой отчетности", description="Рецептурный препарат строгого учета")

                ]

                # Добавляем каждый объект в сессию базы данных

                for recept in recepts:

                    db.session.add(recept)

                # Сохраняем изменения в базе данных

                db.session.commit()

                print("✅ Начальные типы рецептурности добавлены")

        else:

            # Если все таблицы уже существуют, выводим сообщение об этом

            print("✅ Все необходимые таблицы уже существуют!")





# Проверяем, запущен ли скрипт напрямую (не импортирован)

if __name__ == '__main__':

    # Вызываем функцию создания таблиц

    create_missing_tables()