"""
Файл: init_db.py
Назначение: Минимальный пример инициализации БД и добавления тестовых записей.
Роль в проекте: учебный/проверочный скрипт для первого запуска.

Примечание:
- Скрипт отражает раннюю версию модели Product; оставлен как пример рабочей последовательности
    «создать схему -> добавить данные -> commit».
"""

# Импортируем необходимые компоненты из проекта
# create_app - фабричная функция для создания экземпляра Flask приложения
# db - объект SQLAlchemy, который представляет подключение к базе данных и ORM функционал
from app import create_app, db

# Импортируем модели данных (классы, которые соответствуют таблицам в БД)
# Product - модель для товаров/продуктов
# Category - модель для категорий товаров
from app.models import Product, Category

# Создаем экземпляр Flask приложения, вызывая фабричную функцию
# Это приложение содержит всю конфигурацию, включая настройки подключения к БД
app = create_app()

# with app.app_context() создает контекст приложения
# Это необходимо для работы с Flask-SQLAlchemy вне обработчиков запросов
# Внутри этого блока у нас есть доступ к текущему приложению и его конфигурации
with app.app_context():
    # [БЛОК: создание схемы]
    # db.create_all() создает все таблицы в базе данных на основе определений моделей
    # Если таблицы уже существуют, команда их не пересоздает (по умолчанию)
    # Это создает структуру БД: таблицы categories и products с колонками,
    # определенными в моделях Category и Product
    db.create_all()

    # [БЛОК: подготовка тестовых сущностей]
    # Явное поэтапное добавление помогает студентам увидеть жизненный цикл ORM-объекта.

    # Создаем экземпляр класса Category (объект категории)
    # name='Лекарства' - название категории
    # description='Лекарственные препараты' - описание категории
    # На этом этапе объект существует только в памяти Python, не в БД
    category1 = Category(name='Лекарства', description='Лекарственные препараты')

    # db.session.add(category1) добавляет объект в сессию базы данных
    # Сессия отслеживает изменения и подготовливает SQL-запросы
    # Объект переходит в состояние pending (ожидающий сохранения)
    db.session.add(category1)

    # Добавьте тестовый продукт
    # Создаем экземпляр класса Product (объект товара)
    # Все поля соответствуют колонкам таблицы products
    product1 = Product(
        name='Аспирин',  # Название товара
        description='Обезболивающее средство',  # Описание товара
        price=150.0,  # Цена (вещественное число)
        quantity=100,  # Количество на складе
        barcode='123456789',  # Штрих-код товара
        manufacturer='Bayer',  # Производитель
        requires_prescription=False,  # Требуется ли рецепт (булево значение)
        category_id=1  # ID категории (внешний ключ к таблице categories)
    )
    # Добавляем продукт в ту же сессию
    db.session.add(product1)

    # commit фиксирует транзакцию; без него изменения останутся только в памяти сессии.
    # db.session.commit() выполняет несколько действий:
    # 1. Генерирует SQL-запросы INSERT для добавленных объектов
    # 2. Отправляет их в базу данных
    # 3. Фиксирует транзакцию (делает изменения постоянными)
    # 4. Присваивает ID объектам (если использовался autoincrement)
    # 5. Переводит объекты из состояния pending в persistent (постоянное)
    db.session.commit()

    # Выводим сообщение об успешном выполнении
    # Это увидит пользователь при запуске скрипта
    print("Тестовые данные добавлены!")