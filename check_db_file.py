"""
Файл: check_db_file.py
Назначение: Проверка физического файла SQLite-базы (путь, наличие, размер, права доступа).
Роль в проекте: low-level диагностика файловой системы, когда ORM-подключение вызывает вопросы.
"""

import os  # Импортируем модуль os для работы с операционной системой (пути, файлы, права доступа)
from app import create_app  # Импортируем функцию создания приложения из основного модуля

app = create_app()  # Создаем экземпляр Flask приложения

with app.app_context():  # Входим в контекст приложения для доступа к его конфигурации
    # [БЛОК: нормализация пути к БД]
    # SQLite URI может быть относительным; приводим к абсолютному пути для надёжной проверки.

    # Извлекаем путь к файлу базы данных из конфигурации Flask
    # SQLALCHEMY_DATABASE_URI выглядит как 'sqlite:///путь_к_файлу'
    # Метод .replace('sqlite:///', '') удаляет префикс 'sqlite:///' и оставляет только путь
    db_path = app.config['SQLALCHEMY_DATABASE_URI'].replace('sqlite:///', '')

    # Проверяем, является ли путь абсолютным (начинается с корневого каталога)
    if not os.path.isabs(db_path):
        # Если путь относительный, делаем его абсолютным путем
        # app.instance_path - это путь к папке instance приложения
        # (обычно это /путь_к_проекту/instance/)
        # os.path.join() объединяет пути правильным для ОС способом
        db_path = os.path.join(app.instance_path, db_path)

    # Выводим информацию о путях для диагностики
    print(f"📁 Путь к файлу БД: {db_path}")  # Полный путь к файлу базы данных
    print(f"📁 Папка instance: {app.instance_path}")  # Путь к папке instance

    # [БЛОК: ветвление по существованию файла]
    # if/else здесь проще и понятнее, чем исключения по `open`,
    # потому что нужно ещё обработать отсутствие папки instance.

    # Проверяем, существует ли файл базы данных по вычисленному пути
    if os.path.exists(db_path):
        # Если файл существует, выводим информацию о нем
        print(f"✅ Файл БД существует")
        # Получаем размер файла в байтах с помощью os.path.getsize()
        print(f"📏 Размер файла: {os.path.getsize(db_path)} байт")
        # Получаем права доступа к файлу:
        # os.stat(db_path).st_mode - возвращает метаданные файла, включая права доступа
        # oct() преобразует число в восьмеричную систему
        # [-3:] берет последние 3 символа (права для владельца, группы, остальных)
        print(f"🔐 Права доступа: {oct(os.stat(db_path).st_mode)[-3:]}")
    else:
        # Если файл не существует, сообщаем об этом
        print(f"❌ Файл БД НЕ существует по указанному пути")

        # Проверяем, существует ли папка instance
        if os.path.exists(app.instance_path):
            print(f"✅ Папка instance существует")
            # Если папка существует, проверяем права доступа к ней
            print(f"🔐 Права на папку: {oct(os.stat(app.instance_path).st_mode)[-3:]}")
        else:
            print(f"❌ Папка instance не существует")
            # Если папки instance нет, создаем её
            # os.makedirs() создает все недостающие директории в пути
            # exist_ok=True означает, что если папка уже существует, ошибки не будет
            os.makedirs(app.instance_path, exist_ok=True)
            print(f"✅ Папка instance создана")