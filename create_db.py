"""
Файл: create_db.py
Назначение: Первичная инициализация БД и заполнение тестовыми данными.
Роль в проекте: bootstrap-скрипт для локальной разработки/демонстрации.

Важно:
- Скрипт отражает историческую структуру проекта и может не совпадать с текущими моделями 1:1.
- Используется как учебный пример сценария «создать таблицы + сидировать данные».
"""

# Импортируем необходимые компоненты из приложения
from app import create_app, db  # create_app - фабрика для создания экземпляра приложения, db - объект SQLAlchemy для работы с БД
from app.models import Category, Product, Recept  # Импортируем модели данных (категории, товары, типы рецептурности)

# Создаем экземпляр Flask-приложения с помощью фабричной функции
app = create_app()

# Создаем контекст приложения для работы с БД
# Это необходимо, так как вне контекста приложения нет доступа к настройкам и подключениям
with app.app_context():
    # [БЛОК: инициализация схемы]
    # create_all() создаёт отсутствующие таблицы на основе моделей, но не изменяет уже существующие
    # Альтернатива: Alembic/Flask-Migrate для управляемых версионированных миграций
    db.create_all()
    print("Таблицы созданы!")

    # [БЛОК: защита от повторного сидинга]
    # Проверяем, есть ли уже данные в таблице Category
    # Если данные есть, пропускаем заполнение, чтобы не создавать дубликаты
    if Category.query.count() == 0:
        print("Создаем тестовые данные...")

        # Создаем категории рецептурности (типы отпуска лекарств)
        # Каждый элемент кортежа содержит: (название, описание)
        recepts_data = [
            ("Без рецепта", "Препараты, отпускаемые без рецепта врача"),
            ("По рецепту", "Рецептурные препараты"),
            ("Рецептурный строгий учет", "Препараты строгого учета, наркотические средства"),
        ]

        recepts = []  # Список для хранения созданных объектов Recept
        # Цикл для создания объектов Recept из данных
        for name, desc in recepts_data:
            recept = Recept(name=name, description=desc)  # Создаем объект модели
            recepts.append(recept)  # Добавляем в список для возможного использования
            db.session.add(recept)  # Добавляем объект в сессию БД

        db.session.commit()  # Сохраняем все изменения в БД
        print(f"Создано {len(recepts)} типов рецептурности")

        # Создаем категории товаров (остается без изменений)
        categories_data = [
            ("Обезболивающие", "Лекарства для снятия боли"),
            ("Антибиотики", "Антибактериальные препараты"),
            ("Витамины", "Витамины и БАДы"),
            ("Уход за кожей", "Косметические средства"),
            ("Медицинские изделия", "Бинты, пластыри и т.д.")
        ]

        categories = []  # Список для хранения созданных объектов Category
        # Цикл для создания объектов Category из данных
        for name, desc in categories_data:
            cat = Category(name=name, description=desc)  # Создаем объект модели
            categories.append(cat)  # Добавляем в список
            db.session.add(cat)  # Добавляем в сессию БД

        db.session.commit()  # Сохраняем в БД
        print(f"Создано {len(categories)} категорий")

        # Создаем товары с указанием типа рецептурности
        # Каждый кортеж содержит: (название, цена, количество, ID категории, производитель, штрих-код, ID типа рецептурности)
        products_data = [
            ("Аспирин", 150.50, 100, 1, "Bayer", "123456789012", 1),      # Без рецепта
            ("Амоксициллин", 320.75, 50, 2, "Sandoz", "234567890123", 2), # По рецепту
            ("Витамин C", 450.00, 200, 3, "Solgar", "345678901234", 1),   # Без рецепта
            ("Крем для рук", 280.25, 150, 4, "Nivea", "456789012345", 1),  # Без рецепта
            ("Бинт стерильный", 120.00, 300, 5, "Hartmann", "567890123456", 1), # Без рецепта
            ("Нурофен", 280.00, 80, 1, "Reckitt", "678901234567", 1),      # Без рецепта
            ("Цитрамон", 95.50, 120, 1, "Фармстандарт", "789012345678", 1), # Без рецепта
        ]

        # [БЛОК: заполнение таблицы товаров]
        # Распаковка кортежа делает порядок полей явным
        # Альтернатива: словари с именованными ключами, что безопаснее к изменению порядка
        for name, price, quantity, cat_id, manufacturer, barcode, recept_id in products_data:
            product = Product(
                name=name,                    # Название товара
                price=price,                   # Цена
                quantity=quantity,              # Количество на складе
                category_id=cat_id,             # Внешний ключ к таблице категорий
                manufacturer=manufacturer,      # Производитель
                barcode=barcode,                 # Штрих-код
                recept_id=recept_id              # Внешний ключ к таблице типов рецептурности
            )
            db.session.add(product)  # Добавляем товар в сессию БД

        db.session.commit()  # Сохраняем все товары в БД
        print(f"Создано {len(products_data)} товаров")
        print("Тестовые данные успешно добавлены!")
    else:
        # Если в таблице Category уже есть данные
        print("В базе данных уже есть данные")

    # Выводим статистику базы данных после всех операций
    print(f"\nСтатистика базы данных:")
    print(f"Типов рецептурности: {Recept.query.count()}")  # Количество записей в Recept
    print(f"Категорий: {Category.query.count()}")          # Количество записей в Category
    print(f"Товаров: {Product.query.count()}")             # Количество записей в Product