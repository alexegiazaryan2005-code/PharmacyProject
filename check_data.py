"""
Файл: check_data.py
Назначение: Проверка содержимого ключевых таблиц (типы рецептурности и связь с товарами).
Роль в проекте: учебно-диагностический скрипт для валидации данных после миграций.
"""

# Импортируем функцию create_app для создания экземпляра приложения
# create_app - это фабричная функция, которая инициализирует Flask приложение
from app import create_app, db

# Импортируем модели данных Product (Товары) и Recept (Рецептурность)
# Эти модели соответствуют таблицам в базе данных
from app.models import Product, Recept

# Создаем экземпляр Flask приложения, вызывая фабричную функцию create_app
# Это инициализирует приложение со всей его конфигурацией
app = create_app()

# Создаем контекст приложения для работы с базой данных
# В Flask многие операции (включая запросы к БД) требуют активного контекста приложения
with app.app_context():
    # Выводим заголовок для проверки данных в базе
    print("Проверка данных в базе:")

    # Выводим заголовок для всех типов рецептурности
    print("\nВсе типы рецептурности:")

    # Выполняем запрос к базе данных: получаем все записи из таблицы Recept
    # Query.all() возвращает все объекты Recept из базы данных
    for recept in Recept.query.all():
        # Для каждого типа рецептурности выводим:
        # - recept.id: уникальный идентификатор записи
        # - recept.name: название типа рецептурности
        print(f"  {recept.id}: {recept.name}")

    # Выводим заголовок для товаров и их рецептурности
    print("\nТовары и их рецептурность:")

    # Выполняем запрос к базе данных: получаем все записи из таблицы Product
    for product in Product.query.all():
        # Для каждого товара выводим:
        # - product.id: уникальный идентификатор товара
        # - product.name: название товара
        # - product.recept.name: название типа рецептурности для этого товара
        #   (используется связь между таблицами через внешний ключ)
        #   Если у товара нет рецептурности (product.recept == None),
        #   выводим 'Нет данных'
        print(f"  {product.id}. {product.name}: {product.recept.name if product.recept else 'Нет данных'}")