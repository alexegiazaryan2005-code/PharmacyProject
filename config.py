import os  # Импортируем модуль для работы с операционной системой (нужен для путей и переменных окружения)


class Config:
    """
    Класс централизованной конфигурации Flask-приложения.

    Что моделирует:
        Набор параметров окружения и инфраструктуры (секреты, БД, загрузка файлов).

    ООП-принципы:
        - **Инкапсуляция**: все параметры собраны в одном месте.
        - **Наследование**: можно создать DevelopmentConfig/ProductionConfig,
          переопределив только отличающиеся параметры.

    Магические методы:
        - Не переопределяются, так как класс используется как контейнер констант.
    """

    # SECRET_KEY - секретный ключ для подписи сессий и CSRF-токенов
    # os.environ.get('SECRET_KEY') - пытается получить значение из переменной окружения
    # or 'hard-to-guess-string' - если переменная окружения не найдена, использует запасной вариант
    # В продакшене обязательно нужно устанавливать SECRET_KEY в окружении!
    SECRET_KEY = os.environ.get('SECRET_KEY') or 'hard-to-guess-string'

    # basedir - абсолютный путь к директории, где находится этот файл
    # os.path.abspath() - преобразует путь в абсолютный
    # os.path.dirname(__file__) - получает директорию текущего файла
    # __file__ - специальная переменная, содержащая путь к текущему файлу
    basedir = os.path.abspath(os.path.dirname(__file__))

    # SQLALCHEMY_DATABASE_URI - строка подключения к базе данных
    # 'sqlite:///' - префикс для SQLite базы данных
    # os.path.join(basedir, 'instance', 'pharmacy.db') - создаёт путь к файлу БД
    # База будет создана в папке 'instance' рядом с приложением
    SQLALCHEMY_DATABASE_URI = 'sqlite:///' + os.path.join(basedir, 'instance', 'pharmacy.db')

    # SQLALCHEMY_TRACK_MODIFICATIONS - отключает отслеживание изменений объектов
    # False - recommended (рекомендуется) для экономии памяти
    # Эта функция редко нужна в реальных проектах
    SQLALCHEMY_TRACK_MODIFICATIONS = False

    # UPLOAD_FOLDER - папка для загружаемых файлов
    # os.path.join(basedir, 'uploads') - создаёт путь к папке uploads в корне проекта
    UPLOAD_FOLDER = os.path.join(basedir, 'uploads')

    # MAX_CONTENT_LENGTH - максимальный размер загружаемого файла в байтах
    # 16 * 1024 * 1024 - математическое выражение: 16 мегабайт
    # 1024 байта = 1 килобайт, 1024 килобайта = 1 мегабайт
    # Таким образом: 16 * 1024 * 1024 = 16,777,216 байт = 16MB
    MAX_CONTENT_LENGTH = 16 * 1024 * 1024  # 16MB максимальный размер файла